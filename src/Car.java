public class Car {
   private String marca;
   private String name;
   private int speed;
   void setMarca (String marca){
      this.marca=marca;

   }
   String getMarca(){
      return marca;
   }
   void setName( String name){
      this.name=name;
   }
   String getName(){
      return name;
   }
   void setSpeed(int speed){
      this.speed=speed;
   }
   int getSpeed(){
      return speed;
   }
   }
